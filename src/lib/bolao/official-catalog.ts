export function groupCatalogByGrupo(matches: OfficialCatalogMatch[]) {
  const byGroup = new Map<string, OfficialCatalogMatch[]>();
  for (const match of matches) {
    const group = byGroup.get(match.grupo);
    if (group) {
      group.push(match);
    } else {
      byGroup.set(match.grupo, [match]);
    }
  }
  return [...byGroup.entries()]
    .sort((a, b) => a[0].localeCompare(b[0], "pt-BR"))
    .map(([grupo, jogos]) => ({
      grupo,
      jogos: [...jogos].sort((a, b) => a.ordem - b.ordem),
    }));
}
